LIST P=16F877A
    INCLUDE<P16F877A.INC>
    
    SAY1 EQU H'020'
    SAY2 EQU H'021'
 
    SAY_1 EQU H'022'
    SAY_2 EQU H'023'
    SAY_3 EQU H'024'
 
 SARI EQU H'025'
 KIRMIZI EQU H'026'
 YESIL EQU H'027'
 
    ORG 0X00

 
    BANKSEL TRISB
    CLRF TRISB
 
    BANKSEL PORTB
    CLRF PORTB
    
    ;;;
    MOVLW b'10000001'
    MOVWF KIRMIZI
 
    MOVLW b'01000010'
    MOVWF SARI
 
    MOVLW b'00100100'
    MOVWF YESIL
 ;;;;;;;;;;;;;;;;;;;;;;;;
E11 MOVFW KIRMIZI
    MOVWF PORTB
    CALL GECIK_KIRMIZI
 
    ;CALL PORT_TEMIZLE
    
    MOVFW SARI
    MOVWF PORTB
    
    CALL GECIK_SARI
    
   ;CALL PORT_TEMIZLE
    MOVFW YESIL
    MOVWF PORTB
    
    CALL GECIK_YESIL
    
    ;CALL PORT_TEMIZLE
    GOTO E11
 ;;;;;;;;;;;;;;;;;;;;;
 ;PORT TEMIZLE
PORT_TEMIZLE
    MOVLW .0
    MOVWF PORTB
    RETURN
 ;YAKLASIK 5 SANIYE
 GECIK_SARI
    MOVLW .1
    MOVWF SAY_1
    
Z_0 MOVLW .255
    MOVWF SAY_2
 
Z1 MOVLW .255
    MOVWF SAY_3
Z2 DECFSZ SAY_3,F
    GOTO Z2
 
    DECFSZ SAY_2,F
    GOTO Z1
    DECFSZ SAY_1,F
    GOTO Z_0
    RETURN
 
 ;YAKLASIK 9 SAN?YE
 GECIK_KIRMIZI
    MOVLW .0
    MOVWF SAY_1
 
E_0  MOVLW .255
    MOVWF SAY_2
 
E_1 MOVLW .255
    MOVWF SAY_3
E_2 DECFSZ SAY_3 ,F
    GOTO E_2
    DECFSZ SAY_2,F
    GOTO E_1
    DECFSZ SAY_1,F
    GOTO E_0
    RETURN
    
    ;YAKLASIK 8 SANIYE
GECIK_YESIL:
    MOVLW .1
    MOVWF SAY_1
    
E_11 MOVLW .255
    MOVWF SAY_2
    
E_22 MOVLW .255
    MOVWF SAY_3
    
E_33 DECFSZ SAY_3,F
    GOTO E_33
    
    DECFSZ SAY_2,F
    GOTO E_22
    DECFSZ SAY_1,F
    GOTO E_11
    RETURN
    END
 
 