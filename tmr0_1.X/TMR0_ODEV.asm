


LIST P=16F877A
    #INCLUDE <P16F877A.INC>
    DEGER EQU 23H
    ORG 0
    GOTO ANA_PROGRAM
    ORG 4
    GOTO KESME
 
ILK_ISLEM:
    BSF STATUS,5
    BCF STATUS,6
    MOVLW B'00000111'
    MOVWF OPTION_REG
    BSF INTCON,5
    BSF INTCON,GIE
    MOVLW H'00'
    MOVWF TRISB
    BCF STATUS,5
    MOVWF PORTB
    MOVWF DEGER
    MOVWF TMR0
    RETURN
    
KESME:
    BCF INTCON,2
    MOVLW .61
    MOVWF TMR0
    RLF DEGER,1O
    MOVFW DEGER
    MOVWF PORTB
    RETFIE
    
ANA_PROGRAM:
    CALL ILK_ISLEM
SONSUZ:
    GOTO SONSUZ
   ; GOTO $
    END


