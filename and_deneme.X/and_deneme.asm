list p=16f877a
    INCLUDE<P16F877A.INC>
    DENEME EQU 0X020
    SAY1 EQU 0X021
    SAY2 EQU 0X022
    ORG 0X00
    BANKSEL TRISB
    CLRF TRISB
     
    BANKSEL PORTB
    CLRF PORTB
     
     
    MOVLW B'01110001'
    MOVWF DENEME
d1:
    MOVLW B'10011100'
    MOVWF PORTB
    CALL DELAY
    ANDWF DENEME,W
    CALL DELAY
    MOVWF PORTB
    goto d1
DELAY:
    MOVLW .255
    MOVWF SAY1
    
    MOVLW .255
    MOVFW SAY2
    
    DECFSZ SAY2
    GOTO $-1
    DECFSZ SAY1
    GOTO $-5
    RETURN
    END   
